-- TabiPrep: 日本人向け海外旅行準備情報アプリ
-- Supabase テーブル定義

CREATE TABLE IF NOT EXISTS countries (
  id                        SERIAL PRIMARY KEY,
  country_name_ja           TEXT NOT NULL,           -- 国名（日本語）例：アメリカ
  country_name_en           TEXT NOT NULL,           -- 国名（英語）例：United States
  country_code              TEXT NOT NULL UNIQUE,    -- 2文字コード 例：US
  country_flag_emoji        TEXT,                    -- 国旗絵文字 例：🇺🇸

  -- カテゴリ① 入国要件
  visa_required             BOOLEAN NOT NULL DEFAULT false,
  visa_on_arrival           BOOLEAN NOT NULL DEFAULT false,
  esta_required             BOOLEAN NOT NULL DEFAULT false,
  visa_notes                TEXT,
  passport_validity_months  INTEGER,                 -- 必要な残存有効期間（月数）
  return_ticket_required    BOOLEAN NOT NULL DEFAULT false,
  entry_documents           TEXT,                    -- 入国書類の説明

  -- カテゴリ② 電気・電源
  plug_type                 TEXT,                    -- 例：A, C, BF
  voltage                   INTEGER,                 -- 例：220
  plug_image_url            TEXT,

  -- カテゴリ③ 通信・SIM
  sim_airport_available     BOOLEAN,
  esim_supported            BOOLEAN,
  wifi_quality              TEXT,                    -- 良好/普通/不安定

  -- カテゴリ④ お金・決済
  currency_name             TEXT,
  currency_code             TEXT,
  tip_culture               TEXT,
  card_usage                TEXT,
  exchange_notes            TEXT,

  -- カテゴリ⑤ 安全・健康
  safety_level              INTEGER CHECK (safety_level >= 0 AND safety_level <= 4),
  safety_notes              TEXT,
  tap_water_safe            BOOLEAN,
  vaccination_required      TEXT,
  insurance_required        BOOLEAN NOT NULL DEFAULT false,

  -- カテゴリ⑥ 持ち込み禁止・注意品
  prohibited_items          TEXT,

  -- カテゴリ⑦ 文化・マナー
  culture_notes             TEXT,

  -- カテゴリ⑧ 緊急連絡先
  emergency_police          TEXT,
  emergency_ambulance       TEXT,
  emergency_fire            TEXT,
  embassy_phone             TEXT,
  embassy_address           TEXT,

  -- メタ情報
  last_updated              DATE,
  is_manually_verified      BOOLEAN NOT NULL DEFAULT false,

  created_at                TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  updated_at                TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

-- 更新日時の自動更新トリガー
CREATE OR REPLACE FUNCTION update_updated_at()
RETURNS TRIGGER AS $$
BEGIN
  NEW.updated_at = NOW();
  RETURN NEW;
END;
$$ LANGUAGE plpgsql;

CREATE TRIGGER set_updated_at
  BEFORE UPDATE ON countries
  FOR EACH ROW EXECUTE FUNCTION update_updated_at();

